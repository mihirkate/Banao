const mongoose = require("mongoose");
const Schema = mongoose.Schema;
const MONGO_URL = "mongodb://localhost:27017/users-intern-platform";
main()
  .then(() => {
    console.log("connected to DB");
  })
  .catch((err) => {
    console.log(err);
  });

async function main() {
  await mongoose.connect(MONGO_URL);
}

const LoginSchema = new Schema({
  username: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
    expires: 3600, // this is the expiry time
  },
});

const User = mongoose.model("users", LoginSchema);
module.exports = User;
